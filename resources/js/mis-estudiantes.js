//DATATABLES LISTA DE SECCIONES INICIALIZACION
let dataTable = $('#tabla_misEstudiantes').DataTable({
    ajax: {
        method: "POST",
        url: "index.php?pagina=mis-estudiantes",
        data: { listarMisEstudiantes: 'listarMisEstudiantes' },
    },
    columns: [
        { data: 'id_seccion' },
        { data: 'id_materia' },
        { data: 'cedula' },
        {
            data: null,
            render: function (data, type, row, meta) {

                let div = `<div class="d-flex align-items-center">
                <div class="fs-2 me-3"><i class="bi bi-person"></i></div>
                <div class="mb-0">
                  <h6 class="mb-0 fst-italic">${data.codigo}</h6>
                  <p class="mb-0">${data.nombre} ${data.apellido}</p>
                </div>
                </div>`

                return div;
            }
        },
        {
            data: null,
            render: function (data, type, row, meta) {

                let div = `<div class="notaNombreMateria">${data.nombreMateria} (Nivel ${data.nivelAcademico})</div>`

                return div;
            }
        },
        { data: 'nombreSeccion' },
        {
            data: null,
            render: function (data, type, row, meta) {

                let div
                if (data.nota == '') {
                    div = `<button class="agregarNota btn btn-secondary">AGREGAR NOTA</button>`
                } else {
                    div = `<button class="verNotaAgregada btn btn-primary">VER NOTA <i class="bi bi-calculator-fill"></i></button>
                    <button class="eliminarNota btn btn-danger"><i class="bi bi-file-earmark-minus-fill"></i></button>`
                }

                return div;
            }
        },
    ],
    responsive: true,
    language: spanish,
});

dataTable.column(0).visible(false);
dataTable.column(1).visible(false);
dataTable.column(2).visible(false);


//AGREGAR NOTA AL ESTUDIANTE DINAMICAMENTE POR JS
$('#tabla_misEstudiantes tbody').on('click', '.agregarNota', function () {
    const datos = dataTable.row($(this).parents()).data();
    //let elemento = $(this)[0].parentElement.parentElement;

    Swal.fire({
        title: 'Asigne la nota del estudiante',
        icon: 'question',
        input: 'range',
        inputLabel: 'Deslize la barra',
        confirmButtonText: `Agregar`,
        confirmButtonColor: '#0078FF',
        showDenyButton: true,
        denyButtonText: `Cancelar`,
        denyButtonColor: 'grey',
        inputAttributes: {
            min: 0,
            max: 20,
            step: 1
        },
        inputValue: 0,
        preConfirm: (value) => {
            if (value == 0) {
                Swal.showValidationMessage('No puedes agregar "0" como nota');
            }
        }
    }).then((result) => {
        if (result.isConfirmed) {
            const data = {
                agregarNota: 'agregarNota',
                notaCIestudiante: datos.cedula,
                notaIDmateria: datos.id_materia,
                notaIDseccion: datos.id_seccion,
                notaEstudiante: result.value,
            };
            $.post("index.php?pagina=mis-estudiantes", data, function (response) {
                console.log(response);

                dataTable.ajax.reload();

                Swal.fire({
                    icon: 'success',
                    title: '¡Nota agregada!',
                    showConfirmButton: false,
                    timer: 1700,
                })
            });
        }
    })
})

//VER LA NOTA DEL ESTUDIANTE SELECCIONADO
$('#tabla_misEstudiantes tbody').on('click', '.verNotaAgregada', function () {
    const datos = dataTable.row($(this).parents()).data();
    //let elemento2 = $(this)[0].parentElement.parentElement;
    let nombreEstudiante = datos.nombre;
    let nombreMateria = datos.apellido;

    const data = {
        verNota: 'verNota',
        notaCIestudianteRef: datos.cedula,
        notaIDmateriaRef: datos.id_materia,
        notaIDseccionRef: datos.id_seccion,
    };

    $.post("index.php?pagina=mis-estudiantes", data).done(function (data) {
        Swal.fire({
            html: '<span>' + nombreEstudiante + '</span><h5>Materia: ' + nombreMateria + '</h5><h1>' + data + '/20</h1>',
            inputLabel: 'Puedes cambiar la nota aqui si deseas',
            input: 'range',
            confirmButtonText: `Actualizar`,
            confirmButtonColor: '#0078FF',
            showDenyButton: true,
            denyButtonText: `Cancelar`,
            denyButtonColor: 'grey',
            inputAttributes: {
                min: 0,
                max: 20,
                step: 1
            },
            inputValue: data,
        }).then((result) => { //EN TAL CASO QUE QUIERA ACTUALIZARLA; ENTRA AQUI MISMO Y HACE ESO JEJE
            if (result.isConfirmed) {
                const data = {
                    actualizarNota: 'actualizarNota',
                    notaCIestudiante2: datos.cedula,
                    notaIDmateria2: datos.id_materia,
                    notaIDseccion2: datos.id_seccion,
                    notaNueva: result.value,
                };
                $.post("index.php?pagina=mis-materiasEstudiante", data, function (response) {
                    var resp = JSON.parse(response);
                    if (resp == 'actualizada') {
                        dataTable.ajax.reload();
                        
                        Swal.fire({
                            icon: 'success',
                            title: '¡Nota actualizada!',
                            showConfirmButton: false,
                            timer: 2000,
                        })
                    } else {
                        Swal.fire({
                            icon: 'error',
                            title: 'DEGENEADO',
                            text: 'No puedes actualizar su nota de materia porque ya se encuentra asociada a su nota final',
                            showConfirmButton: true,
                            confirmButtonColor: '#0078FF',
                        })
                    }
                });
            }
        })
    });

})

//ELIMINAR LA NOTA DE LA MATERIA DEL ESTUDIANTE SELECCIONADO
$('#tabla_misEstudiantes tbody').on('click', '.eliminarNota', function () {
    const datos = dataTable.row($(this).parents()).data();
    //let elemento3 = $(this)[0].parentElement.parentElement;

    const data = {
        eliminarNota: 'eliminarNota',
        cedulaEstudianteRef2: datos.cedula,
        idMateriaRef2: datos.id_materia,
        idSeccionRef2: datos.id_seccion,
    };

    Swal.fire({
        icon: 'warning',
        iconColor: 'red',
        title: 'AVISO',
        text: '¿Estas segur@ de eliminar su nota asignada?',
        confirmButtonText: `Si, eliminar`,
        confirmButtonColor: '#0078FF',
        showDenyButton: true,
        denyButtonText: `Cancelar`,
        denyButtonColor: 'grey',
    }).then((result) => {
        if (result.isConfirmed) {
            $.post("index.php?pagina=mis-estudiantes", data, function (response) {
                var resp = JSON.parse(response);
                if (resp == 'true') {

                    dataTable.ajax.reload();
                    
                    Swal.fire({
                        icon: 'success',
                        title: '¡Nota eliminada correctamente!',
                        showConfirmButton: false,
                        timer: 1700,
                    })
                } else {
                    Swal.fire({
                        icon: 'error',
                        iconColor: 'red',
                        title: 'AVISO',
                        text: 'No puedes eliminar la nota de este estudiante porque ya se encuentra asociada a su nota final',
                        showConfirmButton: true,
                        confirmButtonColor: '#0078FF',
                    })
                }
            });
        }
    })
})
